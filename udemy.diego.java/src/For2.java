public class For2 {
    public static void main(String[] args) {

        for (int i=0; i<5; i++) {
            System.out.println("O valor de i: " + i);
        }

        System.out.println();

        for (int i=4; i>=0; i--) {
            System.out.println("O valor de i: " + i);
        }

    }
}
